                                    <p><strong>Cook the Pasta:</strong><p>Bring a large pot of salted water to a boil. Add the linguine or spaghetti and cook according to package instructions until al dente. Drain the pasta and set aside.</p></p><p><strong>Prepare the Shrimp:</strong><p>Pat the shrimp dry with paper towels. Season with salt and pepper.</p></p><p><strong>Cook the Shrimp:</strong><p>In a large skillet, heat 1 tablespoon of olive oil over medium-high heat.</p><p>Add the shrimp to the skillet in a single layer. Cook for about 2 minutes on each side until they turn pink and are just cooked through. Remove the shrimp from the skillet and set aside.</p></p><p><strong>Make the Scampi Sauce:</strong><p>In the same skillet, add the remaining tablespoon of olive oil and the unsalted butter. Once the butter is melted, add the minced garlic and red pepper flakes (if using). Saut√© for about 1 minute until the garlic is fragrant.</p><p>Pour in the white wine and chicken broth, scraping up any browned bits from the bottom of the skillet. Bring the mixture to a simmer and cook for 2-3 minutes until the liquid reduces slightly.</p><p>Stir in the freshly squeezed lemon juice.</p></p><p><strong>Combine Pasta and Shrimp:</strong><p>Add the cooked pasta to the skillet and toss to coat it with the sauce.</p><p>Return the cooked shrimp to the skillet and toss to combine. Allow everything to heat through for about 1-2 minutes.</p></p><p><strong>Season and Serve:</strong><p>Season with additional salt and pepper to taste.</p><p>Stir in the chopped fresh parsley.</p><p>Serve the shrimp scampi pasta immediately, garnished with grated Parmesan cheese and lemon wedges on the side.</p></p>
