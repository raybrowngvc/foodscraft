                            <p>
                                <strong>Sauté Aromatics:</strong>
                                <p>In a large pot, heat olive oil over medium heat. Add chopped onion and cook until softened, about 5 minutes.</p>
                                <p>Add minced garlic and cook for another minute until fragrant.</p>                                
                                <strong>Add Tomatoes and Broth:</strong>
                                <p>Pour in the diced tomatoes and tomato sauce, along with the vegetable or chicken broth. Stir to combine.</p>      
                                
                                <strong>Simmer Soup:</strong>
                                <p>Bring the soup to a simmer over medium-low heat. Let it simmer for about 15-20 minutes to allow the flavors to meld together.</p>                                
                                <strong>Blend the Soup:</strong>
                                <p>Once the soup has simmered, use an immersion blender to puree the soup until smooth. Alternatively, carefully transfer the soup in batches to a blender and blend until smooth, then return it to the pot.</p>
                                <strong>Add Heavy Cream and Basil:</strong>
                                <p>Stir in the heavy cream and chopped fresh basil. Season with salt and pepper to taste. Allow the soup to heat through for another 5-10 minutes.</p>
                                <strong>Serve:</strong>
                                <p>Ladle the creamy tomato basil soup into bowls. Garnish each bowl with a sprinkle of grated Parmesan cheese and a few croutons.</p>
                                <strong>Tips:</strong>
                                <p>Feel free to customize the soup by adding other herbs or spices such as oregano, thyme, or red pepper flakes for a kick of heat.</p>
                                <p>Serve the creamy tomato basil soup as a comforting starter or pair it with a grilled cheese sandwich for a classic and satisfying meal.</p>
