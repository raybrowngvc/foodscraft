                                <p><strong>Cook the Spaghetti:</strong></p>Bring a large pot of salted water to a boil. Add the spaghetti and cook according to the package instructions until al dente. This usually takes about 10-12 minutes. Once cooked, drain the spaghetti and set it aside.<p><br><strong>Prepare the Bolognese Sauce:</strong></p>Heat olive oil in a large skillet or saucepan over medium heat.Add the chopped onion and cook until softened, about 5 minutes.Add the minced garlic and cook for another minute until fragrant.Add the lean minced beef to the skillet and cook, breaking it up with a spoon, until browned and no longer pink.Stir in the chopped tomatoes, tomato paste, and dried oregano. Season with salt and pepper to taste.Reduce the heat to low and simmer the sauce uncovered for 15-20 minutes, stirring occasionally, until it has thickened slightly.<p><br><strong>Combine the Spaghetti and Sauce:</strong></p>Once the sauce has reached the desired consistency, add the cooked spaghetti to the skillet with the Bolognese sauce.Toss the spaghetti with the sauce until it is well coated.<p><br><strong>Serve:</strong></p>Divide the spaghetti Bolognese among serving plates.Garnish each plate with grated Parmesan cheese and fresh basil leaves.Serve hot and enjoy!
