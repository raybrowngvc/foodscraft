                                <strong><p>Preheat the Oven:Preheat your oven to 375°F (190°C).</p></strong><strong><p>Prepare the Chicken:</p></strong>Rinse the chicken under cold water and pat it dry with paper towels.Place the chicken on a cutting board, breast-side up. Use kitchen shears to remove any excess fat or loose skin.Carefully loosen the skin on the chicken breast and thighs by gently sliding your fingers underneath the skin. Be careful not to tear the skin.<strong><p><br>Make the Lemon Garlic Mixture:</p></strong>In a small bowl, combine minced garlic, lemon zest, lemon juice, olive oil, dried thyme, salt, and pepper to form a paste.<strong><p><br>Season the Chicken:</p></strong>Rub the lemon garlic mixture all over the chicken, including under the skin. Make sure to evenly distribute the mixture.Tie the chicken legs together with kitchen twine and tuck the wing tips under the body of the chicken.<strong><p><br>Roast the Chicken:</p></strong>Place the seasoned chicken in a roasting pan or baking dish, breast-side up.Roast the chicken in the preheated oven for 1 to 1 ½ hours, or until the internal temperature reaches 165°F (75°C) when measured with a meat thermometer inserted into the thickest part of the thigh.Baste the chicken with the pan juices every 30 minutes to keep it moist and flavorful.<strong><p><br>Rest and Garnish:</p></strong>Once the chicken is cooked through, remove it from the oven and let it rest for 10 minutes before carving.Garnish the roasted chicken with lemon slices and fresh parsley before serving.
